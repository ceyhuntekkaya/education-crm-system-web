// Belirli bir galeri tipine göre filtreleme
export const getGallerySummaryByType = (
  galleryType: string
): GallerySummaryDto[] => {
  return gallerySummaryMockData.filter(
    (gallery) => gallery.galleryType === galleryType
  );
};

// Öne çıkan galerileri getirme
export const getFeaturedGallerySummaries = (): GallerySummaryDto[] => {
  return gallerySummaryMockData.filter((gallery) => gallery.isFeatured);
};

// En çok görüntülenen galerileri getirme
export const getMostViewedGallerySummaries = (
  limit: number = 5
): GallerySummaryDto[] => {
  return gallerySummaryMockData
    .sort((a, b) => (b.viewCount || 0) - (a.viewCount || 0))
    .slice(0, limit);
};

// Kurum adına göre galeri arama
export const getGallerySummaryByInstitution = (
  institutionName: string
): GallerySummaryDto[] => {
  return gallerySummaryMockData.filter((gallery) =>
    gallery.institutionName
      ?.toLowerCase()
      .includes(institutionName.toLowerCase())
  );
};
